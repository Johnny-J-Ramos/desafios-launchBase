// Verifica se a pessoa é maior de 18 anos
// se sim, deixar entrar, se não, bloquear a entrada
// se a pessoa tiver 17, avisar para
// voltar quando fizer 18 anos

const name = 'Maria';
const idade = 17;

if (idade >= 18) {
  console.log(`Bom dia ${name}, você pode entrar.`);
} else {
  console.log('Você não tem 18 anos, não pode entrar');
}
if (idade === 17) {
  console.log('Volte quando completar 18 anos');
}

